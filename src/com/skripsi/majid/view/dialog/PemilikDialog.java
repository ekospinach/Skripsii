    private void setFillTable() {
        Object data[][] = new Object[pemilikRecords.size()][6];
        int x = 0;
        for (Pemilik pemilik : pemilikRecords) {
            data[x][0] = pemilik.getId();
            data[x][1] = pemilik.getNamaPemilik();
            data[x][2] = pemilik.getAlamat();
            data[x][3] = pemilik.getTanggal();
            data[x][4] = pemilik.getTelepon();
            ++x;
        }
        String[] judul = {"Id", "Nama", "Alamat", "Tanggal", "Telepon"};
        jTablePemilik.setModel(new DefaultTableModel(data, judul));
        jScrollPane2.setViewportView(jTablePemilik);
    }

    private void setClearTextField() {
        jTextFieldNama.setText("");
        jTextAreaAlamat.setText("");
        jTextFieldTelepon.setText("");
    }

