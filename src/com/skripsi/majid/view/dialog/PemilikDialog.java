    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        if (jTextFieldNama.getText().trim().isEmpty() || jTextAreaAlamat.getText().trim().isEmpty() || jTextFieldTelepon.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap diisi semua field");
        } else {
            pemilik = new Pemilik();
            pemilik.setNamaPemilik(jTextFieldNama.getText());
            pemilik.setAlamat(jTextAreaAlamat.getText());
            pemilik.setTanggal(new Date());
            pemilik.setTelepon(jTextFieldTelepon.getText());

            try {
                DBConnection conn = DBConnection.getInstance();
                PemilikService service = new PemilikService(conn.getCon());
                service.insert(pemilik);
                JOptionPane.showMessageDialog(this, "Data Saved!");
                loadRecords();
            } catch (SQLException ex) {
                Logger.getLogger(PemilikDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonTambahActionPerformed

    private void jButtonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUbahActionPerformed
        if (jTablePemilik.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih salah satu baris");
        } else {
            int index = jTablePemilik.getSelectedRow();
            int id = (int) jTablePemilik.getValueAt(index, 0);

            Pemilik pemilik = new Pemilik();
            pemilik.setId(id);
            pemilik.setNamaPemilik(jTextFieldNama.getText());
            pemilik.setTanggal(jXDatePickerTanggal.getDate());
            pemilik.setAlamat(jTextAreaAlamat.getText());
            pemilik.setTelepon(jTextFieldTelepon.getText());

            try {
                DBConnection conn = DBConnection.getInstance();
                PemilikService service = new PemilikService(conn.getCon());
                service.update(pemilik);
                JOptionPane.showMessageDialog(this, "Data Telah Diubah!");
                loadRecords();
            } catch (SQLException ex) {
                Logger.getLogger(PemilikDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonUbahActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        if (jTablePemilik.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih salah satu baris");
        } else {
            int hasil = JOptionPane.showConfirmDialog(this, "Apakah anda yakin akan menghapus data ini?");
            if (hasil == JOptionPane.OK_OPTION) {
                int index = jTablePemilik.getSelectedRow();
                int id = (int) jTablePemilik.getValueAt(index, 0);

                try {
                    DBConnection conn = DBConnection.getInstance();
                    PemilikService service = new PemilikService(conn.getCon());
                    service.delete(id);
                    JOptionPane.showMessageDialog(this, "Data Telah Dihapus!");
                    loadRecords();
                } catch (SQLException ex) {
                    Logger.getLogger(PemilikDialog.class.getName()).log(Level.SEVERE, null, ex);
                }
            }


        }
    }//GEN-LAST:event_jButtonHapusActionPerformed

    private void Search(JTable jt, String keyword) {
        TableModel tm = jt.getModel();
        TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tm);
        sorter.setRowFilter(RowFilter.regexFilter("(?i)"+keyword));
        jt.setRowSorter(sorter);
     }
    
